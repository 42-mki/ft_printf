#include "libft.h"

int		ft_atoi(const char *str)
{
	int	ret;
	int	i;

	ret = 0;
	i = 1;
	while ((9 <= *str && *str <= 13) || *str == ' ')
		str++;
	if (*str == '+' || *str == '-')
		if (*(str++) == '-')
			i *= -1;
	while ('0' <= *str && *str <= '9')
	{
		ret *= 10;
		ret += *(str++) - '0';
	}
	return (ret * i);
}
