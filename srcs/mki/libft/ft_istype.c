int		ft_istype(char c)
{
	if (c == 'c' || c == 's' || c == 'p' || c == 'd' || c == 'i' || c == 'u')
		return (1);
	else if (c == 'x' || c == 'X' || c == '%')
		return (1);
	else if (c == 'n' || c == 'f' || c == 'g' || c == 'e')
		return (1);
	return (0);
}
